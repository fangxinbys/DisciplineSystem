		/// <summary>
		/// 获得数据列表
		/// </summary>
		public List<Maticsoft.Model.tTaskLook> DataTableToList(DataTable dt)
		{
			List<Maticsoft.Model.tTaskLook> modelList = new List<Maticsoft.Model.tTaskLook>();
			int rowsCount = dt.Rows.Count;
			if (rowsCount > 0)
			{
				Maticsoft.Model.tTaskLook model;
				for (int n = 0; n < rowsCount; n++)
				{
					model = dal.DataRowToModel(dt.Rows[n]);
					if (model != null)
					{
						modelList.Add(model);
					}
				}
			}
			return modelList;
		}

